            ActionListener stoper = new Zegar();
        
            Timer zegar = new Timer(1000, stoper);
        
            zegar.start();
        
            String h = ""+kalendarzyk.get(Calendar.HOUR_OF_DAY);
            String m = ""+kalendarzyk.get(Calendar.MINUTE);
